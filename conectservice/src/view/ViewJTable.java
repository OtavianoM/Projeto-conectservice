    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTProdutos.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();

        for (PrestadorServico p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getNome(),
                p.getCnpj(),
                p.getAtuacao(),
                p.getValor()
            });

        }

    }
    public void readJTableForDesc(String desc) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTProdutos.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();

        for (PrestadorServico p : pdao.readForDesc(desc)) {

            modelo.addRow(new Object[]{
               p.getNome(),
                p.getCnpj(),
                p.getAtuacao(),
                p.getValor()
            });

        }

    }

