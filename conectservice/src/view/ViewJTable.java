    private void btnacao(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnacao
        // TODO add your handling code here:
        
          if (jTProdutos.getSelectedRow() != -1 ) {
            txtNome.setText(jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 0).toString());
            txtCnpj.setText(jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 1).toString());
            txtAtuacao.setText(jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 2).toString());
            txtValor.setText(jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 3).toString());

        }
    }//GEN-LAST:event_btnacao

